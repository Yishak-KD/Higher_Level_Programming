#!/usr/bin/python3
"""A Sqaure class"""

from models.rectangle import Rectangle


class Square(Rectangle):
    """
    Class Square that inherits from a Rectangle class
    """

    def __init__(self, size, x=0, y=0, id=None):
        super().__init__(width=size, height=size, x=x, y=y, id=id)

    def __str__(self):
        return f"[Square] ({self.id}) {self.x}/{self.y} - {self.width}"

    @property
    def size(self):
        return self.width

    @size.setter
    def size(self, value):
        self.width = value
        self.height = value

    def update(self, *args, **kwargs):
        if len(args) != 0:
            try:
                self.id = args[0]
                self.size = args[1]
                self.x = args[2]
                self.y = args[3]
            except IndexError:
                pass

        elif len(args) == 0:
            for k, v in kwargs.items():
                setattr(self, k, v)
